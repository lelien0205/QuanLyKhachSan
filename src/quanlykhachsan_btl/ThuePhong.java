    private void jButton_thueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_thueActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = jTable_dsDichVu.getSelectedRows();
        String maPhong = jComboBox_maPhong.getSelectedItem().toString();
        java.sql.Timestamp thoiDiemYeuCau = new java.sql.Timestamp(System.currentTimeMillis());
        try {
            String maKhachHang = jTextField_maKhachHang.getText();
            Date ngayDen = GenericClass.getDateFromDateChooser(jDateChooser_ngayDen);
            Date ngayDi = GenericClass.getDateFromDateChooser(jDateChooser_ngayDi);

            // Kiểm tra nếu một trong các trường là trống
            if (maKhachHang.isEmpty() || jComboBox_maPhong.getSelectedIndex() < 0 || jDateChooser_ngayDen.getDate() == null || jDateChooser_ngayDi.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else if (ngayDen.after(ngayDi)) {
                // Kiểm tra nếu ngày đến sau ngày đi
                JOptionPane.showMessageDialog(this, "Ngày đến phải trước ngày đi.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                // Thêm dữ liệu vào cơ sở dữ liệu
                String query = "INSERT INTO phieuden(MaPhieu, MaKhachHang, MaPhong, NgayDen, NgayDi) VALUES (?, ?, ?, ?, ?)";
                try (java.sql.PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                    preparedStatement.setString(1, maPhieuDen);
                    preparedStatement.setString(2, maKhachHang);
                    preparedStatement.setString(3, maPhong);
                    preparedStatement.setDate(4, new java.sql.Date(ngayDen.getTime()));
                    preparedStatement.setDate(5, new java.sql.Date(ngayDi.getTime()));

                    // Thực hiện câu lệnh SQL
                    preparedStatement.executeUpdate();
                }
                clear_input();
                GenericClass.updateTinhTrangPhong(connection, maPhong, "Đã thuê");
                JOptionPane.showMessageDialog(this, "Thuê phòng " + maPhong + " thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (HeadlessException | NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi đặt phòng.", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
        if (selectedRows != null){
            themDichVu(selectedRows, maPhong, thoiDiemYeuCau);
        }
    }//GEN-LAST:event_jButton_thueActionPerformed


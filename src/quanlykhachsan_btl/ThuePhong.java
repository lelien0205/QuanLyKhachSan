    private void loadMaPhongTrong() {
        try {
            jComboBox_maPhong.removeAllItems();
            String query = "SELECT MaPhong FROM phong WHERE TinhTrang = 'Đã đặt'";
            try (Statement statement = connection.createStatement(); ResultSet resultSet = statement.executeQuery(query)) {

                while (resultSet.next()) {
                    String maPhong = resultSet.getString("MaPhong");
                    jComboBox_maPhong.addItem(maPhong);
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void themDichVu(int[] selectedRow, String maPhong, java.sql.Timestamp thoiDiemYeuCau) {
        try {
            int[] _selectedRows = selectedRow;
            for (int rowIndex : _selectedRows) {
                String maDichVu = jTable_dsDichVu.getValueAt(rowIndex, 0).toString();
                String queryInsert = "INSERT INTO phong_dichvu(MaPhong, MaDichVu, ThoiDiemYeuCau) VALUES (?, ?, ?)";
                try (java.sql.PreparedStatement preparedStatement = connection.prepareStatement(queryInsert)) {
                    preparedStatement.setString(1, maPhong);
                    preparedStatement.setString(2, maDichVu);
                    preparedStatement.setTimestamp(3, thoiDiemYeuCau);

                    preparedStatement.executeUpdate();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        } catch (HeadlessException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi đặt phòng.", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }



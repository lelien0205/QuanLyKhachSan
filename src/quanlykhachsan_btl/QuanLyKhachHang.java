    private void jButton_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_xoaActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = jTable_dsKhach.getSelectedRows();

        if (selectedRows.length > 0) {
            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa " + selectedRows.length + " khách hàng đã chọn?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                try {
                    for (int i = selectedRows.length - 1; i >= 0; i--) {
                        String maPhong = jTable_dsKhach.getValueAt(selectedRows[i], 6).toString();
                        String maKhachHang = jTable_dsKhach.getValueAt(selectedRows[i], 2).toString();
                        String deleteQuery = "DELETE FROM khachhang WHERE MaKhachHang = ?";
                        try (java.sql.PreparedStatement preparedStatement = connection.prepareStatement(deleteQuery)) {
                            preparedStatement.setString(1, maKhachHang);
                            preparedStatement.executeUpdate();
                        }
                        DefaultTableModel model = (DefaultTableModel) jTable_dsKhach.getModel();
                        GenericClass.updateTinhTrangPhong(connection, maPhong, "Trống");
                        model.removeRow(selectedRows[i]);
                    }
                    JOptionPane.showMessageDialog(this, "Đã xóa " + selectedRows.length + " khách hàng.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    Logger.getLogger(QuanLyPhong.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "Lỗi khi xóa khách hàng: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ít nhất một dòng để xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton_xoaActionPerformed


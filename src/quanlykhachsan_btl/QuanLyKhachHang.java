    private void loadMaPhongTrong() {
        try {
            jComboBox_maPhong.removeAllItems();
            String query = "SELECT MaPhong FROM phong WHERE TinhTrang = 'Trống'";
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);

            while (resultSet.next()) {
                String maPhong = resultSet.getString("MaPhong");
                jComboBox_maPhong.addItem(maPhong);
            }

            resultSet.close();
            statement.close();
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyKhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void themPhieuDen(String maPhieuDen, String maKhachHang, String maPhong, Date ngayDen, Date ngayDi) {
        try {
            String query = "INSERT INTO phieuden(MaPhieu, MaKhachHang, MaPhong, NgayDen, NgayDi) VALUES (?, ?, ?, ?, ?)";
            try (java.sql.PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, maPhieuDen);
                preparedStatement.setString(2, maKhachHang);
                preparedStatement.setString(3, maPhong);
                preparedStatement.setDate(4, new java.sql.Date(ngayDen.getTime()));
                preparedStatement.setDate(5, new java.sql.Date(ngayDi.getTime()));

                preparedStatement.executeUpdate();
            }
            clear_input();
        } catch (HeadlessException | NumberFormatException | SQLException ex) {
            Logger.getLogger(QuanLyPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


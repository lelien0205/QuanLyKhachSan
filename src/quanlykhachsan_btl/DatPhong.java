    private void loadPhong(String query) {
        try {
            // Clear the table before loading new data
            DefaultTableModel model = (DefaultTableModel) jTable_dsPhong.getModel();
            model.setRowCount(0);

            // Create a PreparedStatement with the provided query
            java.sql.PreparedStatement pst = connection.prepareStatement(query);

            // Set values for the parameters in the SQL statement
            pst.setString(1, jComboBox_loaiPhong.getSelectedItem().toString());
            pst.setString(2, jComboBox_hangPhong.getSelectedItem().toString());

            // Execute the query
            ResultSet resultSet = pst.executeQuery();

            while (resultSet.next()) {
                // Retrieve data from the result set and add it to the table model
                Object[] col = new Object[5];
                col[0] = resultSet.getObject(1);
                col[1] = resultSet.getObject(2);
                col[2] = resultSet.getObject(3);
                col[3] = resultSet.getObject(4);
                col[4] = resultSet.getObject(5);
                model.addRow(col);
            }

            // Close the result set and the prepared statement
            resultSet.close();
            pst.close();
        } catch (SQLException ex) {
            Logger.getLogger(DatPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


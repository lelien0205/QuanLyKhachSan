    private void jButton_datActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_datActionPerformed
        // TODO add your handling code here:
        try {
            // Lấy dữ liệu từ các component
            String hoTen = jTextField_hoTen.getText();
            String maKhachHang = jTextField_maKhachHang.getText();
            String soDienThoai = jTextField_soDienThoai.getText();
            Date ngayDen = GenericClass.getDateFromDateChooser(jDateChooser_ngayDen);
            Date ngayDi = GenericClass.getDateFromDateChooser(jDateChooser_ngayDi);
            int soNguoi = Integer.parseInt(jTextField_soNguoi.getText());

            // Kiểm tra nếu một trong các trường là trống
            if (hoTen.isEmpty() || maKhachHang.isEmpty() || soDienThoai.isEmpty() || soNguoi < 0) {
                JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else if (ngayDen.after(ngayDi)) {
                // Kiểm tra nếu ngày đến sau ngày đi
                JOptionPane.showMessageDialog(this, "Ngày đến phải trước ngày đi.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                // Thêm dữ liệu vào cơ sở dữ liệu
                String query = "INSERT INTO khachhang(MaKhachHang, HoTen, SoDienThoai, NgayDen, NgayDi, SoNguoi, MaPhong) VALUES (?, ?, ?, ?, ?, ?, ?)";
                try (java.sql.PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                    preparedStatement.setString(1, maKhachHang);
                    preparedStatement.setString(2, hoTen);
                    preparedStatement.setString(3, soDienThoai);
                    preparedStatement.setDate(4, new java.sql.Date(ngayDen.getTime()));
                    preparedStatement.setDate(5, new java.sql.Date(ngayDi.getTime()));
                    preparedStatement.setInt(6, soNguoi);
                    preparedStatement.setString(7, maPhong);

                    // Thực hiện câu lệnh SQL
                    preparedStatement.executeUpdate();
                }
                clear_input();
                GenericClass.updateTinhTrangPhong(connection, maPhong, "Đã đặt");
                JOptionPane.showMessageDialog(this, "Đặt phòng " + maPhong + " thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (HeadlessException | NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi đặt phòng.", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_datActionPerformed


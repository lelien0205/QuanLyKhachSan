    private Double layChiPhi(String query, String parameter) {
        double chiPhi = 0;
        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setObject(1, parameter);
            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                while (resultSet.next()) {
                    chiPhi += resultSet.getDouble(1);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return chiPhi;
    }

    private String layThongTin(String query, String parameter) {
        String thongTin = null;
        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setObject(1, parameter);
            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                while (resultSet.next()) {
                    thongTin = resultSet.getString(1);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return thongTin;
    }

    private void laySoNgay(String parameter) {
        String query = "SELECT * FROM khachhang WHERE MaPhong = ?";
        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setString(1, parameter);
            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                if (resultSet.next()) {
                    ngayDen = (LocalDateTime) resultSet.getObject(4);
                    ngayDi = (LocalDateTime) resultSet.getObject(5);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        Period period = Period.between(ngayDen.toLocalDate(), ngayDi.toLocalDate());
        soNgay = period.getDays();
    }


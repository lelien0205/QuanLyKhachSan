    private void jTable_dsPhongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_dsPhongMouseClicked
        // TODO add your handling code here:
        visibility = true;
        int selectedRow = jTable_dsPhong.getSelectedRow();

        jLabel_tenKhachHang.setText(jTable_dsPhong.getValueAt(selectedRow, 0).toString());
        jLabel_canCuoc.setText(jTable_dsPhong.getValueAt(selectedRow, 1).toString());
        maPhong = jTable_dsPhong.getValueAt(selectedRow, 2).toString();
        laySoNgay(maPhong);
        jLabel_ngayDen.setText(ngayDen.toLocalDate().format(formatter));
        jLabel_ngayDi.setText(ngayDi.toLocalDate().format(formatter));
        jLabel_ngaySuDung.setText(Integer.toString(soNgay));
        jLabel_thongTinPhong.setText(layThongTin("SELECT HangPhong FROM phong WHERE MaPhong = ?", maPhong) + ", " + layThongTin("SELECT LoaiPhong FROM phong WHERE MaPhong = ?", maPhong));
        chiPhiPhong = layChiPhi("SELECT GiaThue FROM phong WHERE MaPhong = ?", maPhong) * soNgay;
        chiPhiDichVu = layChiPhi("SELECT dv.ChiPhi FROM phong_dichvu pdv JOIN dichvu dv ON pdv.MaDichVu = dv.MaDichVu WHERE pdv.MaPhong = ?", maPhong);
        thanhTien = chiPhiDichVu + chiPhiPhong;
        jLabel_chiPhiPhong.setText(Double.toString(chiPhiPhong) + " ₫");
        jLabel_thongTinDichVu.setText(layThongTin("SELECT COUNT(*) FROM phong_dichvu WHERE MaPhong = ?", maPhong));
        jLabel_chiPhiDichVu.setText(Double.toString(chiPhiDichVu) + " ₫");
        jLabel_thanhTien.setText(Double.toString(thanhTien) + " ₫");
        setVisibility(visibility);
    }//GEN-LAST:event_jTable_dsPhongMouseClicked

    private void jButton_thanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_thanhToanActionPerformed
        // TODO add your handling code here:
        String query = "INSERT INTO thanhtoan (MaThanhToan, MaKhachHang, MaPhong, TienPhong, TienDichVu, TongTien, ThoiDiemThanhToan, MaNhanVien) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        if (thanhTien != 0) {
            java.sql.Timestamp thoiDiemThanhToan = new java.sql.Timestamp(System.currentTimeMillis());
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setObject(1, GenericClass.generateRandomString("TT", 10000, 90000));
                preparedStatement.setObject(2, jLabel_canCuoc.getText());
                preparedStatement.setObject(3, maPhong);
                preparedStatement.setObject(4, chiPhiPhong);
                preparedStatement.setObject(5, chiPhiDichVu);
                preparedStatement.setObject(6, thanhTien);
                preparedStatement.setObject(7, thoiDiemThanhToan);
                preparedStatement.setObject(8, GenericClass.maNhanVien);
                if (preparedStatement.executeUpdate() >= 1) {
                    GenericClass.updateCSDL(connection, "DELETE FROM phieuden WHERE MaPhong = ?", maPhong);
//                    GenericClass.updateCSDL(connection, "DELETE FROM khachhang WHERE MaKhachHang = ?", jLabel_canCuoc.getText());
                    GenericClass.updateCSDL(connection, "DELETE FROM phong_dichvu WHERE MaPhong = ?", maPhong);
                    GenericClass.updateTinhTrangPhong(connection, maPhong, "Trống");
                    int luaChon = JOptionPane.showConfirmDialog(
                            this,
                            "Thanh toán phòng " + maPhong + " thành công" + "\n" + "Bạn có muốn in hóa đơn?",
                            "Xác nhận",
                            JOptionPane.YES_NO_OPTION);
                    if (luaChon == JOptionPane.YES_OPTION) {
//                        exportToExcel(maPhong, maPhong, chiPhiPhong, chiPhiDichVu, thanhTien);
                        JOptionPane.showMessageDialog(this, "It got some shit that need to be fixed", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Thanh toán phòng " + maPhong + " không thành công", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Lỗi khi thanh toán.\n" + ex.getMessage(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chọn khách hàng để thanh toán", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_thanhToanActionPerformed


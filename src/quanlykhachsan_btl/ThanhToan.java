    private void loadData() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable_dsPhong.getModel();
            model.setRowCount(0);

            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT kh.*, p.TinhTrang FROM khachhang kh JOIN phong p ON kh.MaPhong = p.MaPhong WHERE p.TinhTrang = 'Đã thuê'");

            while (resultSet.next()) {
                Object[] col = new Object[3];
                col[0] = resultSet.getObject(2);
                col[1] = resultSet.getObject(1);
                col[2] = resultSet.getObject(7);
                model.addRow(col);
            }

            resultSet.close();
            statement.close();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }

